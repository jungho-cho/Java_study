
public class FourArithmetic {
	public static void main(String args[])
	{
		int a = 10;
		int b = 5;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		System.out.println(7 % 3);
		System.out.println(3 % 7);
		
		int i = 0;
		int j = 10;
		i++;
		j--;
		
		System.out.println(i);
		System.out.println(j);
		
		i = 0;
		System.out.println(i++);
		System.out.println(i);
		
		i=0;
		System.out.println(++i);
		System.out.println(i);
	}
}
